/* ---- Low-level I2C helpers ---- */
static void oled_cmd(uint8_t cmd)
{
    uint8_t buf[2] = {CTRL_CMD, cmd};
    i2c_master_write_to_device(OLED_I2C_PORT, OLED_ADDR, buf, 2, pdMS_TO_TICKS(10));
}

static void oled_data(const uint8_t *data, size_t len)
{
    /* Prepend the data control byte */
    uint8_t buf[len + 1];
    buf[0] = CTRL_DATA;
    memcpy(buf + 1, data, len);
    i2c_master_write_to_device(OLED_I2C_PORT, OLED_ADDR, buf, len + 1, pdMS_TO_TICKS(50));
}

/* ---- Public API ---- */
void ssd1306_init(void)
{
    /* Initialise I2C_NUM_1 for the OLED */
    i2c_config_t cfg = {
        .mode          = I2C_MODE_MASTER,
        .sda_io_num    = OLED_SDA_PIN,
        .scl_io_num    = OLED_SCL_PIN,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
    };
    cfg.master.clk_speed = OLED_I2C_FREQ;
    i2c_param_config(OLED_I2C_PORT, &cfg);
    i2c_driver_install(OLED_I2C_PORT, I2C_MODE_MASTER, 0, 0, 0);

    /* SSD1306 init sequence */
    oled_cmd(0xAE);         /* display off          */
    oled_cmd(0xD5); oled_cmd(0x80); /* clock divide  */
    oled_cmd(0xA8); oled_cmd(0x3F); /* multiplex 64  */
    oled_cmd(0xD3); oled_cmd(0x00); /* display offset */
    oled_cmd(0x40);                  /* start line 0  */
    oled_cmd(0x8D); oled_cmd(0x14); /* charge pump on */
    oled_cmd(0x20); oled_cmd(0x00); /* horizontal addr mode */
    oled_cmd(0xA1);                  /* seg remap     */
    oled_cmd(0xC8);                  /* COM scan dir  */
    oled_cmd(0xDA); oled_cmd(0x12); /* COM pins      */
    oled_cmd(0x81); oled_cmd(0xCF); /* contrast      */
    oled_cmd(0xD9); oled_cmd(0xF1); /* pre-charge    */
    oled_cmd(0xDB); oled_cmd(0x40); /* VCOMH         */
    oled_cmd(0xA4);                  /* RAM display   */
    oled_cmd(0xA6);                  /* normal (not inverted) */
    oled_cmd(0xAF);                  /* display on    */
}

void ssd1306_clear(void)
{
    static const uint8_t zeros[128] = {0};
    for (uint8_t p = 0; p < 8; p++) {
        oled_cmd(0xB0 | p);   /* page          */
        oled_cmd(0x00);        /* col low  = 0  */
        oled_cmd(0x10);        /* col high = 0  */
        oled_data(zeros, 128);
    }
}

void ssd1306_set_cursor(uint8_t col, uint8_t page)
{
    oled_cmd(0xB0 | (page & 0x07));
    oled_cmd(0x00 | (col & 0x0F));
    oled_cmd(0x10 | (col >> 4));
}

void ssd1306_print(const char *str)
{
    while (*str) {
        uint8_t ch = (uint8_t)*str++;
        if (ch < 0x20 || ch > 0x7E) ch = 0x20;
        const uint8_t *glyph = font5x7[ch - 0x20];
        oled_data(glyph, 5);
        uint8_t space = 0x00;
        oled_data(&space, 1); /* 1-pixel gap between chars */
    }
}

void ssd1306_printf_at(uint8_t col, uint8_t page, const char *fmt, ...)
{
    char buf[22]; /* max 21 chars + null on a 128-px wide screen at 6px/char */
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);
    ssd1306_set_cursor(col, page);
    ssd1306_print(buf);
}
